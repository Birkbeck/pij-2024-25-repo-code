public class S2Example2 {
    public static void main(String[] args) {
        int num = 5;
        System.out.println(num);
        num = num + 2;
        System.out.println(num);
        num = num / 3 * 6;
        System.out.println(num);
        System.out.println(7 + 15 % 4);
        num = 24 / 3 / 4;
        System.out.println(num);
        num = 24 / (num / 4);
        System.out.println(num);
    }
}
